		<h2>How To Play</h2>
		<p>The game is simple - A random rip will be selected from the database and will be played to you.<br>When listening to the rip, you will need to guess the following:</p>
		<ul>
			<li>The game the rip is from</li>
			<li>The joke(s) in the rip</li>
		</ul>
		<p>You'll earn points based on the accuracy of your guesses and the speed at which you submitted them. See <a href="">scoring</a> below for more info.</p>

		<h3>Game Modes</h3>
		<p>There are a number of game modes available:</p>
		<ul>
			<li><b>Standard</b><br>
				<p>A random rip is chosen and you have to guess the game and/or jokes in the rip.</p>
			</li>
			<li><b>Veteran</b><br>
				<p>Along with the game and jokes, you'll have to guess the secondary (album) name of the rip and the ripper who made the rip.<br>
					This mode is more suited to those who are familiar with the ripping community!</p>
			</li>
		</ul>
		<p>Each game mode also has difficulty settings allowing you to see show or hide how many jokes or rippers are available for the rip.</p>
		<h3>Scoring</h3>
		<p>Each game consists of a three rounds. At the end of the rounds, your score will be tallied up and given to you. The score is comprised of the following:</p>
		<ul>
			<li>Correct game (100pts)</li>
			<li>Correct joke (50pts per joke)</li>
			<li>Correct ripper (80pts per ripper)</li>
			<li>Correct secondary name (150pts)</li>
			<li>Answer speed<br>
				300pts, -1pt every second after 10 seconds has passed.<br>
				If 310 seconds has passed, 0 pts are awarded.
			</li>
		</ul>

		<h4>Example Scoring</h4>
		<p>Assume the Standard game mode is being played.</p>
		<ul>
			<li>The game was guessed correctly</li>
			<li>The rip has 2 jokes, only one of which was guessed correctly.</li>
			<li>The guesses were submitted after 18 seconds of the round starting</li>
		</ul>

		<p>The score would be calculated as follows:</p>
		<ul>
			<li>Game: +100pts</li>
			<li>Jokes (1/2): +50pts</li>
			<li>18 secs: (300s - 8s = ) +292pts</li>
		</ul>
		<span>Total: 442pts</span>
